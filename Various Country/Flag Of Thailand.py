    turtle.forward(800)
    turtle.right(90)
    turtle.forward(90)
    turtle.right(90)
    turtle.forward(800)
    turtle.end_fill()
    turtle.penup()
    turtle.goto(-500, -40)
    turtle.pendown()
    turtle.begin_fill()
    turtle.fillcolor("BLUE")
    turtle.left(90)
    turtle.forward(150)
    turtle.right(90)
    turtle.forward(800)
