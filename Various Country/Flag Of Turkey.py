#flag of turkey

from turtle import *
setup(720,1080)
speed(5)
pu()
goto(-350,275)
pd()

color("red")
pd()
begin_fill()
for i in range(2):
    fd(700)
    rt(90)
    fd(550)
    rt(90)
end_fill()

pu()
goto(-50,0)
pu()
backward(75)
lt(90)
fd(125)
lt(90)
color("white")
pd()
begin_fill()
circle(127.5)
end_fill()
pu()
goto(-87,105.5)
color("red")
pd()
begin_fill()
circle(107)
end_fill()
pu()
color("white")
goto(50,-35)
rt(20)
begin_fill()
pd()
for x in range(5):
    fd(100)
    rt(144)
end_fill()

pu()
goto(0,275)
rt(154)
color("black")
write("Flag of Turkey",font=("Arial",20,"normal"))
pu()
goto(0,800)
done()
