#eyes drawing in Python
from turtle import *
tracer(10)
pensize(5)
lt(45)
up()
goto(150,0)
down()
for i in range(170):
    color("#FFCC99")
    up()
    circle(170-i,91)
    down()
    rt(1)
    circle(170+i,90)
up()
goto(78,63)
down()
color("#4477c9")
begin_fill()
circle(90)
end_fill()

up()
goto(40,100)
down()
color("black")
begin_fill()
circle(40)
end_fill()
ht()
done()
