from turtle import *
setup(950,1010,0,0)
pensize(10)
color("Pink")
rt(90)
circle(25,180)
up()
goto(0,0)
down()
pensize(5)
fd(300)
up()
goto(150,140)
down()
begin_fill()
circle(150,180)
lt(90)
fd(300)
end_fill()
color("white")
begin_fill()
def Little_Umbrella_circle():
    circle(25)
lt(90)
Little_Umbrella_circle()
up()
goto(100,140)
Little_Umbrella_circle()
up()
goto(50,140)
Little_Umbrella_circle()
up()
goto(0,140)
Little_Umbrella_circle()
up()
goto(-50,140)
Little_Umbrella_circle()
up()
goto(-100,140)
Little_Umbrella_circle()
end_fill()

