import turtle as t
t.pencolor('white')
t.circle(150)
t.penup()
t.circle(150,-48)
t.pendown()
t.fillcolor('#C9002B')
t.begin_fill()
t.pencolor('#C9002B')
t.circle(150,-163)
t.left(105)
for i in range(10):
    t.forward(30)
    t.right(5)
t.forward(9)
t.end_fill()
t.pencolor('white')
t.left(180)
t.right(70.9)
t.penup()
t.circle(150,4)
t.pendown()
t.begin_fill()
t.fillcolor('#004B93')
t.pencolor('#004B93')
t.circle(150,170)
t.left(160)
for i in range(40):
    t.forward(4)
    t.right(2)
t.right(5)
t.forward(50)
t.right(8)
for i in range(26):
    t.forward(5)
    t.left(0.5)
t.end_fill()
t.penup()
t.left(180)
t.forward(100)
t.right(20)
t.forward(180)
t.write("pepsi",move=False,font=("Sans Serif",60, "normal"))
t.hideturtle()
t.done()
