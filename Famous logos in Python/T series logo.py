#T series logo in Pytohn
from turtle import *
pu()
goto(-120,170)
pd()
color("red")
begin_fill()
for i in range(2):
    fd(240)
    rt(90)
    fd(340)
    rt(90)
end_fill()

pu()
goto(-100,150)
pd()
color("white")
pensize(5)

for i in range(2):
    fd(200)
    rt(90)
    fd(300)
    rt(90)

pu()
goto(0,-50)
pd()
circle(80)

pu()
goto(-28,-25)
pd()
color("white")
begin_fill()
fd(30)
lt(80)
fd(75)
rt(80)
fd(30)
lt(80)
fd(25)
lt(100)
fd(90)
lt(80)
fd(25)
lt(100)
fd(30)
rt(100)
fd(75)
end_fill()
pu()
goto(-90,-120)
pd()
pencolor("white")
write("SERIES",font=("Arial",38,"bold"))

ht()
done()
