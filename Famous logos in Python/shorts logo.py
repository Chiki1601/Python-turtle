#shorts logo in Python

from turtle import *
speed(3)
width(4)
pu()
fd(40)
pd()
color("red")
begin_fill()
lt(30)
fd(50)
circle(60,180)
fd(180)
circle(60,180)
rt(180)
fd(50)
circle(60,180)
fd(180)
circle(60,180)
end_fill()
color("white")
penup()
goto(30,-40)
pd()
begin_fill()
fd(100)
rt(120)
fd(100)
rt(120)
fd(100)
end_fill()
t.write("Shorts logo",font=("future",40,"bold"))
t.ht()
done()
