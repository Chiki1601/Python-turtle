#swiggy logo 1 in Python
import turtle

t=turtle.Turtle()
turtle.bgcolor("darkorange")
t.speed(10)

t.color("black")
t.fillcolor("white")
t.begin_fill()
t.penup()
t.goto(0,110)
t.pendown()
t.left(90)
t.forward(35)
t.circle(4,180)
t.forward(45)
t.left(94)
t.circle(-300,10)
t.circle(-60,40)
t.right(37)
t.circle(-60,40)
t.forward(140)
t.right(110)
t.forward(100)
t.left(83)
t.circle(70,-50)
t.left(110)
t.forward(20)
t.circle(4,180)
t.forward(35)
t.left(90)
t.forward(80)
t.left(120)
t.circle(120,-100)
t.circle(70,-120)
t.left(-60)
t.circle(153,-30)
t.end_fill()


t.penup()
t.goto(-140,-170)
t.pendown()
t.color("white")
t.write("SWIGGY",font=("Times",60))

t.hideturtle()
turtle.done()
