#chanel logo IN Python
from turtle import *
speed(5)
width(1)
pu()
goto(-140,-40)
pd()
color("black")
begin_fill()
rt(60)
circle(100,300)
lt(90)
fd(30)
lt(90)
circle(-70,300)
lt(90)
fd(30)
end_fill()
pu()
goto(140,-40)
pd()
begin_fill()
lt(30)
circle(-100,300)
rt(90)
fd(30)
rt(90)
circle(70,300)
rt(90)
fd(30)
end_fill()
pu()
goto(-120,-170)
pd()
style=("arial","50")
write("CHANEL",font=(style))
ht()
done()
