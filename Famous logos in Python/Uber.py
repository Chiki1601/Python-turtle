#Uber logo
import turtle as t
t.color("black")
t.begin_fill()
t.circle(150)
t.end_fill()
t.pu()
t.goto(-150,160)
t.pd()
t.color("White")
t.begin_fill()
t.pencolor("white")
t.fd(115)
t.lt(90)
t.fd(25)
t.rt(90)
for i in range(3):
    t.fd(75)
    t.rt(90)
t.fd(25)
t.lt(90)
t.fd(115)
t.rt(90)
t.fd(25)
t.end_fill()
t.pencolor("black")
t.pu()
t.goto(-150,-150)
t.pd()
t.write("Uber logo",font=("future",40,"bold"))
t.ht()
t.done()
