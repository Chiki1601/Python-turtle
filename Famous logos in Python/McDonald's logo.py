#McDonald's logo in Python

import turtle as t
t.write('by Pooja Patel')
t.bgcolor('red')
t.pencolor('gold')
t.width(25)
t.speed(2)
t.up()
t.setpos(-300,-300)
t.down()
t.fillcolor("gold")
t.begin_fill()
t.fd(40)
t.lt(90)
t.circle(-1200,20)
t.circle(-30,140)
t.circle(-1200,19)
t.seth(0)
t.fd(40)
t.lt(90)
t.circle(-1200,19)
t.circle(-30,140)
t.circle(-1200,20)
t.seth(0)
t.fd(40)
t.lt(90.4)
t.circle(1260,21.5)
t.circle(50,120)
t.circle(500,15)
t.fd(50)
t.rt(133)
t.fd(50)
t.circle(500,15)
t.circle(50,120)
t.circle(1260,21.5)
t.end_fill()
t.done()
