#adidas logo in Python

import turtle
screen = turtle.Screen()
t = turtle.Turtle()
t.speed(11)
t.begin_fill()
t.circle(100,90,200)
t.right(270)
t.circle(100,90,200)
t.end_fill()
t.up()
t.goto(-10,0)
t.down()
t.setheading(45)
t.begin_fill()
t.circle(100,90,200)
t.right(270)
t.circle(100,90,270)
t.end_fill()
t.up()
t.goto(-20,0)
t.down()
t.setheading(90)
t.begin_fill()
t.circle(100,90,200)
t.right(270)
t.circle(100,90,200)
t.end_fill()
t.pensize(10)
t.up()
t.color("white")
t.goto(-200,56)
t.down()
t.forward(400)
t.pensize(10)
t.up()
t.color("white")
t.goto(-200,40)
t.down()
t.forward(400)
t.pensize(10)
t.up()
t.color("white")
t.goto(-200,24)
t.down()
t.forward(400)
screen.mainloop()
