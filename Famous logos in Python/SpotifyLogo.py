#spotify logo in Python

import turtle as t
t.begin_fill()
t.fillcolor('#1DB954')
t.circle(100)
t.end_fill()
t.penup()
t.goto(30,40)
t.pendown()
t.left(130)
t.forward(0)
t.pensize(15)
t.pencolor('black')
t.circle(90,60)
t.penup()
t.goto(40,65)
t.pendown()
t.pencolor('black')
t.pensize(18)
t.right(60) 
t.forward(0)

t.circle(100,60)
t.penup()
t.goto(60,95)
t.pendown()
t.pencolor('black')
t.pensize(20)
t.right(60)
t.forward(0)
t.circle(110,60)
t.done()
