#incognito mode icon in Python
from turtle import *
width(1)
speed(6)
bgcolor("#202124")
color("white")
goto(0,-200)
begin_fill()
circle(200)
end_fill()
goto(-17,-65)
lt(95)
color("#202124")
begin_fill()
circle(50,350)
rt(60)
circle(-40,50)
rt(60)
circle(50,350)
rt(110)
circle(40,50)
end_fill()
goto(-27,-75)
rt(123)
color("white")
begin_fill()
circle(40)
end_fill()
pu()
goto(27,-75)
lt(15)
pd()
begin_fill()
circle(-40)
end_fill()
pu()
goto(-140,-5)
pd()
rt(7)
color("#202124")
begin_fill()
for i in range(2):
    fd(10)
    rt(90)
    fd(280)
    rt(90)
end_fill()
pu()
goto(-100,20)
pd()
begin_fill()
rt(20)
fd(120)
circle(-10,90)
fd(45)
circle(10,40)
fd(45)
circle(-10,90)
fd(120)
rt(110)
fd(200)
end_fill()
pu()
goto(-300,-300)
pd()
color("white")
write("You've gone Incognito!",font = ('Arial',50))

ht()
done()
