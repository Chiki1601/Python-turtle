#home icon in Python

import turtle as t
t.pensize(10)
t.color("black")
t.speed(3)
t.up()
t.goto(-20,-90)
t.down()
t.begin_fill()
t.circle(200)
t.end_fill()
t.up()
t.goto(-90,0)
t.pendown()
t.color("white")
t.begin_fill()
t.fd(50)
t.lt(90)
t.fd(60)
t.rt(90)
t.fd(40)
t.rt(90)
t.fd(60)
t.lt(90)
t.fd(50)
t.lt(90)
t.fd(80)
t.rt(90)
t.fd(30)
t.lt(120)
t.fd(200)
t.lt(120)
t.fd(200)
t.lt(120)
t.fd(30)
t.rt(90)
t.fd(80)
t.end_fill()
t.hideturtle()
t.done()
